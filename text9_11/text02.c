//变态面试题 - 不创建临时变量，实现数字的交换

#include<stdio.h>

int main()
{
	int a = 5;
	int b = 3;
	printf("交换前：\na=%d\nb=%d\n", a, b);

	////加减法 - 缺陷是可能会溢出
	//a = a + b;
	//b = a - b;
	//a = a - b;


	//异或法 - 二进制相同为0，相异为1，缺点：执行效率低，代码可读性低，但是面试会考
	a = a^b;
	//a=101 b=011 产生新a=110
	//产生的这个数与数字a异或会产生数字b，与数字b异或会产生数字a
	b = a^b;
	//a=110 b=011 产生新b=101
	a = a^b;
	//a=110 b=101 产生新a=011
	printf("交换后：\na=%d\nb=%d\n", a, b);

	return 0;
}