//操作符详解 - ~

#include <stdio.h>

//按位取反 ~
int main()
{
	int a = 0;
	//00000000000000000000000000000000
	//11111111111111111111111111111111 - 补码
	//原码取反加1得到补码，补码减1取反得到原码
	//11111111111111111111111111111110 - 反码
	//10000000000000000000000000000001 - 原码（符号位不变，其他位按位取反）
	//-1
	printf("%d\n", ~a);
	return 0;
}